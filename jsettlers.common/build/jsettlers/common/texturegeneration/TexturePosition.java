package jsettlers.common.texturegeneration;

public class TexturePosition {

	public final float left, top, right, bottom;

	public TexturePosition(float left, float top, float right, float bottom) {
		super();
		this.left = left;
		this.top = top;
		this.right = right;
		this.bottom = bottom;
	}

	/**
	 * @return the left
	 */
	public float getLeft() {
		return left;
	}

	/**
	 * @return the top
	 */
	public float getTop() {
		return top;
	}

	/**
	 * @return the right
	 */
	public float getRight() {
		return right;
	}

	/**
	 * @return the bottom
	 */
	public float getBottom() {
		return bottom;
	}

}
